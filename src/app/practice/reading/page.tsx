// Function to select random questions
const selectRandomQuestions = (questions: ReadingQuestion[], count: number): ReadingQuestion[] => {
  const shuffled = questions.sort(() => 0.5 - Math.random());
  return shuffled.slice(0, count);
};

export default function ReadingPracticePage() {
  const [answers, setAnswers] = useState<{ [key: string]: string }>({}); // Store answers for all questions
  const [showResults, setShowResults] = useState(false);
  const [score, setScore] = useState(0);
  const [questions, setQuestions] = useState<ReadingQuestion[]>([]); // State to hold the randomly selected questions

  // Select random questions when the component mounts
  useEffect(() => {
    setQuestions(selectRandomQuestions(readingQuestions, 1)); // Select 1 random passage with its questions
  }, []);

  // Handle answer input change for a specific question
  const handleAnswerChange = (passageId: number, questionId: number, value: string) => {
    setAnswers(prev => ({
      ...prev,
      [`${passageId}-${questionId}`]: value
    }));
  };

  // Handle submitting answers
  const handleSubmit = () => {
    let correctCount = 0;
    let totalQuestions = 0;

    questions.forEach(passage => {
      passage.questions.forEach(question => {
        totalQuestions++;
        // Trim and convert to uppercase for case-insensitive comparison of options
        const userInput = (answers[`${passage.id}-${question.id}`] || '').trim().toUpperCase();
        // Check if the trimmed and uppercased input matches the correct answer option letter
        if (userInput === question.answer.toUpperCase()) {
          correctCount++;
        }
      });
    });

    setScore(correctCount);
    setShowResults(true);
  };

  // Handle resetting the practice
  const handleReset = () => {
    setQuestions(selectRandomQuestions(readingQuestions, 1));
    setAnswers({});
    setShowResults(false);
    setScore(0);
  };

  return (
    <div className="container mx-auto px-4 py-8">
      <h1 className="text-2xl font-bold mb-6">阅读题练习</h1>

      {questions.length > 0 ? (
        questions.map(passage => (
          <div key={passage.id} className="mb-8">
            {/* Reading Passage */}
            <div className="mb-6 p-4 bg-gray-50 rounded">
              <h2 className="text-lg font-semibold mb-4">{passage.title}</h2>
              <p className="whitespace-pre-wrap">{passage.passage}</p>
            </div>

            {/* Questions */}
            <div className="space-y-6">
              {passage.questions.map(question => (
                <div key={question.id} className="border p-4 rounded">
                  <p className="mb-2">{question.id}. {question.stem}</p>
                  {/* Display Options */}
                  <ul className="list-disc list-inside mb-2">
                    {Object.entries(question.options).map(([key, value]) => (
                      <li key={key} className="flex items-center gap-2">
                        <input
                          type="radio"
                          id={`${passage.id}-${question.id}-${key}`}
                          name={`question-${passage.id}-${question.id}`}
                          value={key}
                          checked={answers[`${passage.id}-${question.id}`] === key}
                          onChange={e => handleAnswerChange(passage.id, question.id, e.target.value)}
                          disabled={showResults}
                          className="mr-2"
                        />
                        <label htmlFor={`${passage.id}-${question.id}-${key}`}>
                          {key}. {value}
                        </label>
                      </li>
                    ))}
                  </ul>
                  {showResults && (
                    <p className={`mt-1 ${
                      (answers[`${passage.id}-${question.id}`] || '').trim().toUpperCase() === question.answer.toUpperCase()
                        ? 'text-green-600'
                        : 'text-red-600'
                    }`}>
                      正确答案: {question.answer}
                    </p>
                  )}
                </div>
              ))}
            </div>
          </div>
        ))
      ) : (
        <p>正在加载题目...</p>
      )}

      {/* Submit and Reset Buttons */}
      <div className="mt-6">
        {!showResults ? (
          <button
            onClick={handleSubmit}
            className="bg-black text-white px-4 py-2 rounded hover:bg-gray-800"
            disabled={questions.length === 0}
          >
            提交答案
          </button>
        ) : (
          <div>
            <p className="mb-4">得分: {score}/{questions.reduce((acc, passage) => acc + passage.questions.length, 0)}</p>
            <button
              onClick={handleReset}
              className="bg-red-500 text-white px-4 py-2 rounded hover:bg-red-600"
            >
              重新开始
            </button>
          </div>
        )}
      </div>
    </div>
  );
} 